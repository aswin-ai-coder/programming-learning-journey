#include <stdio.h>   // allows input and output functions like printf

int main()           // main function: program starts running from here
{
    printf("Hello World\n");   // prints text to the screen

    return 0;       // indicates program finished successfully
}



/*
Important Things to Notice
1. #include <stdio.h>
Allows the program to use input/output functions.

2. int main()
The starting point of every C program.

3. printf()
Used to display text on the screen.

4. return 0
Ends the program properly.



Logic Thinking
Even a simple program follows logic:

Start program
Print message
End program

*/
